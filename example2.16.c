#include <stdio.h>
int main(){
    int num1, num2;
    printf("Enter first number  \n");
    scanf("%d", &num1);
    printf("Enter second number  \n");
    scanf("%d", &num2);
    printf("Sum =  %d \n",num1 + num2);
    printf("Difference =  %d \n",num1 - num2);
    printf("Product =  %d \n",num1 * num2);
    printf("Division =  %d \n",num1 / num2);
    printf("Mod =  %d \n",num1 % num2);

}